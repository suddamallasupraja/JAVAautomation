package oops_concepts;

public class Encapsulation {
	private int a;
	private double b;
	private String c;
	//setters and getters method
	void setA(int a)
	{
		this.a=a;
	}
int get()
{
	return a;
}
	double getB() {
	return b;
}
void setB(double b) {
	this.b = b;
}
String getC() {
	return c;
}
 void setC(String c) {
	this.c = c;
}


}

